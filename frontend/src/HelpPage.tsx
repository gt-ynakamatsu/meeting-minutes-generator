/** 初期表示・ブックマーク用: ハッシュが #help なら true */
export function readHelpHash(): boolean {
  if (typeof window === "undefined") return false;
  return window.location.hash.replace(/^#/, "").toLowerCase() === "help";
}

export function openHelpHash(): void {
  window.location.hash = "help";
}

export function clearHelpHash(): void {
  const { pathname, search } = window.location;
  window.history.replaceState(null, "", pathname + search);
}
